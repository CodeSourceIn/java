package in.codesource.programs.java.language_fundamentals.basic_language_elements;

public class IntLiterals {
	public static void main(String[] args) {
		System.out.println("int data type literals");
		System.out.println("----------------------");
		System.out.println("Decimal literals");
		System.out.println("decimal         12 : " + 12);
		System.out.println("decimal       -159 : " + -159);
		System.out.println("");
		System.out.println("Octal literals");
		System.out.println("octal          014 : " + 014);
		System.out.println("octal        -0237 : " + -0237);
		System.out.println("");
		System.out.println("Hexadecimal literals");
		System.out.println("hexadecimal    0xc : " + 0xc);
		System.out.println("hexadecimal  -0x9f : " + -0x9f);
		System.out.println("hexadecimal    0xC : " + 0xC);
		System.out.println("hexadecimal  -0x9F : " + -0x9F);
		System.out.println("hexadecimal    0Xc : " + 0Xc);
		System.out.println("hexadecimal  -0X9f : " + -0X9f);
		System.out.println("hexadecimal    0XC : " + 0XC);
		System.out.println("hexadecimal  -0X9F : " + -0X9F);
		System.out.println("");
		System.out.println("Binary literals");
		System.out.println("binary      0b1100 : " + 0b1100);
		System.out.println("binary -0b10011111 : " + -0b10011111);
		System.out.println("binary      0B1100 : " + 0B1100);
		System.out.println("binary -0B10011111 : " + -0B10011111);
	}
}
