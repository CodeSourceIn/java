package in.codesource.programs.java.language_fundamentals.basic_language_elements;

public class LongLiterals {
	public static void main(String[] args) {
		System.out.println("long data type literals");
		System.out.println("----------------------");
		System.out.println("Decimal literals");
		System.out.println("decimal         12l : " + 12l);
		System.out.println("decimal       -159l : " + -159l);
		System.out.println("decimal         12L : " + 12L);
		System.out.println("decimal       -159L : " + -159L);
		System.out.println("");
		System.out.println("Octal literals");
		System.out.println("octal          014l : " + 014l);
		System.out.println("octal        -0237l : " + -0237l);
		System.out.println("octal          014L : " + 014L);
		System.out.println("octal        -0237L : " + -0237L);
		System.out.println("");
		System.out.println("Hexadecimal literals");
		System.out.println("hexadecimal    0xcl : " + 0xcl);
		System.out.println("hexadecimal  -0x9fl : " + -0x9fl);
		System.out.println("hexadecimal    0xCl : " + 0xCl);
		System.out.println("hexadecimal  -0x9Fl : " + -0x9Fl);
		System.out.println("hexadecimal    0Xcl : " + 0Xcl);
		System.out.println("hexadecimal  -0X9fl : " + -0X9fl);
		System.out.println("hexadecimal    0XCl : " + 0XCl);
		System.out.println("hexadecimal  -0X9Fl : " + -0X9Fl);
		System.out.println("hexadecimal    0xcL : " + 0xcL);
		System.out.println("hexadecimal  -0x9fL : " + -0x9fL);
		System.out.println("hexadecimal    0xCL : " + 0xCL);
		System.out.println("hexadecimal  -0x9FL : " + -0x9FL);
		System.out.println("hexadecimal    0XcL : " + 0XcL);
		System.out.println("hexadecimal  -0X9fL : " + -0X9fL);
		System.out.println("hexadecimal    0XCL : " + 0XCL);
		System.out.println("hexadecimal  -0X9FL : " + -0X9FL);
		System.out.println("");
		System.out.println("Binary literals");
		System.out.println("binary      0b1100l : " + 0b1100l);
		System.out.println("binary -0b10011111l : " + -0b10011111l);
		System.out.println("binary      0B1100l : " + 0B1100l);
		System.out.println("binary -0B10011111l : " + -0B10011111l);
		System.out.println("binary      0b1100L : " + 0b1100L);
		System.out.println("binary -0b10011111L : " + -0b10011111L);
		System.out.println("binary      0B1100L : " + 0B1100L);
		System.out.println("binary -0B10011111L : " + -0B10011111L);
	}
}
