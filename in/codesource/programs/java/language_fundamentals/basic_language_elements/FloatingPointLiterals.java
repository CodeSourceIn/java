package in.codesource.programs.java.language_fundamentals.basic_language_elements;

public class FloatingPointLiterals {
	public static void main(String[] args) {
	System.out.println("double data type literals");
	System.out.println("-------------------------");
	System.out.println("0.0       : " + 0.0);
	System.out.println("0.0D      : " + 0.0D);
	System.out.println(".1122     : " + .1122);
	System.out.println("45.       : " + 45.);
	System.out.println("45.D      : " + 45.D);
	System.out.println(".123D     : " + .123D);
	System.out.println(".123d     : " + .123d);
	System.out.println("0D        : " + 0D);
	System.out.println("1.754E+2  : " + 1.754E+2);
	System.out.println(".732E+1D  : " + .732E+1D);
	System.out.println("");
	System.out.println("float data type literals");
	System.out.println("------------------------");
	System.out.println("0.0f      : " + 0.0f);
	System.out.println("0.0F      : " + 0.0F);
	System.out.println(".1122f    : " + .1122f);
	System.out.println("45.f      : " + 45.f);
	System.out.println("45.F      : " + 45.F);
	System.out.println(".123F     : " + .123F);
	System.out.println(".123f     : " + .123f);
	System.out.println("0f        : " + 0f);
	System.out.println("1.754E+2f : " + 1.754E+2f);
	System.out.println(".732E+1f  : " + .732E+1f);
	}
}
